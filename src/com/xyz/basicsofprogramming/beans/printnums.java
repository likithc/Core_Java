package com.xyz.basicsofprogramming.beans;

public class printnums {

	public static void main(String[] args) {
		System.out.println("Ascending order:");
        for (int i = 1; i <= 9; i++) {
            System.out.print(i + " ");
        }
        
        System.out.println("Descending Order:");
        for(int i =9;i>=1;i--) {
        	System.out.println(i + "");
        }
	}

}
